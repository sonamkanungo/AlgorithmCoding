public class MostFrequentNode {

    Integer number;
    Integer count;

    public MostFrequentNode(int number, int count) {
        this.number = number;
        this.count = count;
    }

    public Integer getNumber() {
        return number;
    }

    public void setNumber(Integer number) {
        this.number = number;
    }

    public Integer getCount() {
        return count;
    }

    public void setCount(Integer count) {
        this.count = count;
    }
}
